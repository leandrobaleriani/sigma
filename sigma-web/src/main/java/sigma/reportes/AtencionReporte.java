package sigma.reportes;

import java.util.Date;

public class AtencionReporte {

	private Date fecha;
	private String nombre;
	private int edad;
	private String sexo;
	private String partido;
	private String diagnostico;
	private String obraSocial;
	private String planPrivadoMutual;
	private String planSeguroPublico;
	private String masDeUno;
	private String ninguno;
	private String field;

	public Date getFecha() {
		return fecha;
	}

	public void setFecha(Date fecha) {
		this.fecha = fecha;
	}

	public String getNombre() {
		return nombre;
	}

	public void setNombre(String nombre) {
		this.nombre = nombre;
	}

	public int getEdad() {
		return edad;
	}

	public void setEdad(int edad) {
		this.edad = edad;
	}

	public String getSexo() {
		return sexo;
	}

	public void setSexo(String sexo) {
		this.sexo = sexo;
	}

	public String getPartido() {
		return partido;
	}

	public void setPartido(String partido) {
		this.partido = partido;
	}

	public String getDiagnostico() {
		return diagnostico;
	}

	public void setDiagnostico(String diagnostico) {
		this.diagnostico = diagnostico;
	}

	public String getObraSocial() {
		return obraSocial;
	}

	public void setObraSocial(String obraSocial) {
		this.obraSocial = obraSocial;
	}

	public String getPlanPrivadoMutual() {
		return planPrivadoMutual;
	}

	public void setPlanPrivadoMutual(String planPrivadoMutual) {
		this.planPrivadoMutual = planPrivadoMutual;
	}

	public String getPlanSeguroPublico() {
		return planSeguroPublico;
	}

	public void setPlanSeguroPublico(String planSeguroPublico) {
		this.planSeguroPublico = planSeguroPublico;
	}

	public String getMasDeUno() {
		return masDeUno;
	}

	public void setMasDeUno(String masDeUno) {
		this.masDeUno = masDeUno;
	}

	public String getNinguno() {
		return ninguno;
	}

	public void setNinguno(String ninguno) {
		this.ninguno = ninguno;
	}

	public String getField() {
		return field;
	}

	public void setField(String field) {
		this.field = field;
	}

}
